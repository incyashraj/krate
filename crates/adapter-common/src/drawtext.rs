/// Pixel width of a string at the given integer scale.
pub fn text_width(text: &str, scale: u32) -> u32 {
    (text.chars().count() as u32) * GLYPH_CELL_W * scale
}

/// Pixel height of one text line at the given integer scale.
pub fn text_height(scale: u32) -> u32 {
    GLYPH_CELL_H * scale
}

/// Draw `text` into an ARGB `buffer` of `width`x`height` at top-left `pos`.
///
/// Unknown characters render as spaces. Clipping is per pixel, so partial
/// off-surface text draws safely.
pub fn draw_text(
    buffer: &mut [u32],
    width: u32,
    height: u32,
    pos: (i32, i32),
    scale: u32,
    color: u32,
    text: &str,
) {
    let (x, y) = pos;
    let scale = scale.max(1);
    let mut pen_x = x;
    for ch in text.chars() {
        let idx = (ch as u32).wrapping_sub(32);
        if idx < 95 {
            let glyph = &FONT_5X7[idx as usize];
            for (col, bits) in glyph.iter().enumerate() {
                for row in 0..7u32 {
                    if bits & (1 << row) != 0 {
                        for sx in 0..scale {
                            for sy in 0..scale {
                                let px = pen_x + (col as u32 * scale + sx) as i32;
                                let py = y + (row * scale + sy) as i32;
                                if px >= 0 && py >= 0 && (px as u32) < width && (py as u32) < height
                                {
                                    buffer[(py as u32 * width + px as u32) as usize] = color;
                                }
                            }
                        }
                    }
                }
            }
        }
        pen_x += (GLYPH_CELL_W * scale) as i32;
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn text_metrics_scale_linearly() {
        assert_eq!(text_width("Click me", 1), 8 * GLYPH_CELL_W);
        assert_eq!(text_width("Click me", 2), 16 * GLYPH_CELL_W);
        assert_eq!(text_height(2), 2 * GLYPH_CELL_H);
    }

    #[test]
    fn drawing_marks_pixels_and_clips_safely() {
        let (w, h) = (64u32, 16u32);
        let mut buffer = vec![0u32; (w * h) as usize];
        draw_text(&mut buffer, w, h, (1, 1), 1, 0xFFFFFFFF, "Hi");
        assert!(buffer.contains(&0xFFFFFFFF));
        // Clipping: drawing far off-surface must not panic or write.
        let before = buffer.clone();
        draw_text(&mut buffer, w, h, (-500, -500), 1, 0xFFFF0000, "off");
        draw_text(&mut buffer, w, h, (10_000, 10_000), 1, 0xFFFF0000, "off");
        assert_eq!(buffer, before);
    }
}
