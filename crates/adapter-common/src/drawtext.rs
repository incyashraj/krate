//! Minimal built-in bitmap text for the drawn-widget fallback.
//!
//! A public-domain-style 5x7 pixel font covering printable ASCII, rendered
//! directly into an ARGB framebuffer. This is a deliberate placeholder: it
//! makes drawn widgets legible today with zero dependencies, and the vello
//! renderer replaces it with real typography behind the same draw pass.
//! Glyph shapes are utilitarian; a wrong pixel is cosmetic, not functional.

/// Width of one glyph cell in font pixels (5 drawn + 1 spacing).
pub const GLYPH_CELL_W: u32 = 6;
/// Height of one glyph cell in font pixels.
pub const GLYPH_CELL_H: u32 = 8;

