use std::env;
use std::fs;
use std::path::{Path, PathBuf};
use std::process::{Command, Output};
use std::time::{Duration, Instant, SystemTime, UNIX_EPOCH};

use anyhow::{bail, Context, Result};
use sha2::{Digest, Sha256};

const DEFAULT_RUNS: usize = 15;
const DEFAULT_WARMUPS: usize = 3;
const FIXED_TIME_MILLIS: &str = "1234567890";
const FIXED_LOCALE: &str = "en-US";
const FIXED_TIMEZONE: &str = "UTC";
const EXPECTED_STDOUT: &str =
    "app=layer36-clock\ntimezone=UTC\nlocale=en-US\ndate=1970-01-15 06:56\n";

fn main() -> Result<()> {
    let config = Config::parse(env::args().skip(1))?;
    let report = record_cli_startup(&config)?;
    let markdown = report.to_markdown();

    if let Some(parent) = config.output.parent() {
        fs::create_dir_all(parent)
            .with_context(|| format!("create output directory {}", parent.display()))?;
    }
    fs::write(&config.output, markdown)
        .with_context(|| format!("write {}", config.output.display()))?;
    println!("wrote {}", config.output.display());

    Ok(())
}

#[derive(Debug, Clone)]
struct Config {
    layer36: PathBuf,
    manifest: PathBuf,
    wasm: PathBuf,
    output: PathBuf,
    runs: usize,
    warmups: usize,
}

impl Config {
    fn parse(args: impl IntoIterator<Item = String>) -> Result<Self> {
        let mut layer36 = None;
        let mut manifest = None;
        let mut wasm = None;
        let mut output = PathBuf::from("target/phase2-benchmark-evidence/cli-startup.md");
        let mut runs = DEFAULT_RUNS;
        let mut warmups = DEFAULT_WARMUPS;

        let mut args = args.into_iter();
        while let Some(arg) = args.next() {
            match arg.as_str() {
                "--layer36" => layer36 = Some(next_path(&mut args, "--layer36")?),
                "--manifest" => manifest = Some(next_path(&mut args, "--manifest")?),
                "--wasm" => wasm = Some(next_path(&mut args, "--wasm")?),
                "--output" => output = next_path(&mut args, "--output")?,
                "--runs" => runs = next_usize(&mut args, "--runs")?,
                "--warmups" => warmups = next_usize(&mut args, "--warmups")?,
                "--help" | "-h" => {
                    println!(
                        "Usage: record-phase2-cli-startup [--layer36 <path>] [--manifest <path>] [--wasm <path>] [--runs <n>] [--warmups <n>] [--output <md>]"
                    );
                    std::process::exit(0);
                }
                _ => bail!("unknown argument `{arg}`"),
            }
        }

        if runs == 0 {
            bail!("--runs must be greater than zero");
        }

        let config = Self {
            layer36: layer36.unwrap_or_else(default_layer36_path),
            manifest: manifest.unwrap_or_else(|| PathBuf::from("apps/layer36-clock/manifest.toml")),
            wasm: wasm.unwrap_or_else(|| {
                PathBuf::from("apps/layer36-clock/target/wasm32-wasip1/release/layer36_clock.wasm")
            }),
            output,
            runs,
            warmups,
        };
        config.validate_paths()?;
        Ok(config)
    }

    fn validate_paths(&self) -> Result<()> {
        for (name, path) in [
            ("layer36", &self.layer36),
            ("manifest", &self.manifest),
            ("wasm", &self.wasm),
        ] {
            if !path.is_file() {
                bail!(
                    "{name} path does not exist or is not a file: {}",
                    path.display()
                );
            }
        }
        Ok(())
    }
}

fn default_layer36_path() -> PathBuf {
    PathBuf::from("target")
        .join("release")
        .join(format!("layer36{}", env::consts::EXE_SUFFIX))
}

fn next_path(args: &mut impl Iterator<Item = String>, flag: &str) -> Result<PathBuf> {
    args.next()
        .map(PathBuf::from)
        .with_context(|| format!("{flag} requires a path"))
}

fn next_usize(args: &mut impl Iterator<Item = String>, flag: &str) -> Result<usize> {
    let value = args
        .next()
        .with_context(|| format!("{flag} requires a value"))?;
    value
        .parse::<usize>()
        .with_context(|| format!("{flag} must be an unsigned integer"))
}

#[derive(Debug, Clone)]
struct CliStartupReport {
    generated_unix_seconds: u64,
    git_commit: String,
    host_os: String,
    host_arch: String,
    command: String,
    runs: usize,
    warmups: usize,
    durations: Vec<Duration>,
    stdout_sha256: String,
    stderr_sha256: String,
}

impl CliStartupReport {
    fn to_markdown(&self) -> String {
        let stats = StartupStats::from_durations(&self.durations);
        let mut out = String::new();
        out.push_str("# Phase 2 CLI Startup Evidence\n\n");
        out.push_str("This file is generated by `record-phase2-cli-startup`.\n\n");
        out.push_str("## Host\n\n");
        out.push_str(&format!("- Git commit: `{}`\n", self.git_commit));
        out.push_str(&format!(
            "- Host: `{}` / `{}`\n",
            self.host_os, self.host_arch
        ));
        out.push_str(&format!(
            "- Generated Unix time: `{}`\n",
            self.generated_unix_seconds
        ));
        out.push_str(&format!("- Warmup runs: `{}`\n", self.warmups));
        out.push_str(&format!("- Measured runs: `{}`\n\n", self.runs));

        out.push_str("## Command\n\n");
        out.push_str("```text\n");
        out.push_str(&self.command);
        out.push_str("\n```\n\n");

        out.push_str("## Result\n\n");
        out.push_str(&format!("- Mean: `{}` ns\n", stats.mean_ns));
        out.push_str(&format!("- Median: `{}` ns\n", stats.median_ns));
        out.push_str(&format!("- Min: `{}` ns\n", stats.min_ns));
        out.push_str(&format!("- Max: `{}` ns\n", stats.max_ns));
        out.push_str(&format!("- Stdout SHA-256: `{}`\n", self.stdout_sha256));
        out.push_str(&format!("- Stderr SHA-256: `{}`\n\n", self.stderr_sha256));

        out.push_str("## Samples\n\n");
        out.push_str("Durations are full external CLI process times in nanoseconds.\n\n");
        out.push_str("```text\n");
        for (index, duration) in self.durations.iter().enumerate() {
            out.push_str(&format!("{:02}: {}\n", index + 1, duration.as_nanos()));
        }
        out.push_str("```\n\n");
        out.push_str("## Reading The Result\n\n");
        out.push_str(
            "This is not a microbenchmark. It measures the full `layer36 run` command path for the deterministic clock sample, including process start, argument parsing, manifest handling, runtime setup, component execution, and output collection.\n",
        );
        out
    }
}

#[derive(Debug, Clone)]
struct StartupStats {
    mean_ns: u128,
    median_ns: u128,
    min_ns: u128,
    max_ns: u128,
}

impl StartupStats {
    fn from_durations(durations: &[Duration]) -> Self {
        let mut values = durations.iter().map(Duration::as_nanos).collect::<Vec<_>>();
        values.sort_unstable();
        let sum = values.iter().sum::<u128>();
        let mean_ns = sum / values.len() as u128;
        let median_ns = values[values.len() / 2];
        let min_ns = values[0];
        let max_ns = values[values.len() - 1];

        Self {
            mean_ns,
            median_ns,
            min_ns,
            max_ns,
        }
    }
}

fn record_cli_startup(config: &Config) -> Result<CliStartupReport> {
    let command = command_text(config);

    for _ in 0..config.warmups {
        let output = run_once(config).context("run warmup CLI startup command")?;
        validate_output(&output).context("validate warmup CLI startup output")?;
    }

    let mut durations = Vec::with_capacity(config.runs);
    let mut last_output = None;
    for _ in 0..config.runs {
        let start = Instant::now();
        let output = run_once(config).context("run measured CLI startup command")?;
        let elapsed = start.elapsed();
        validate_output(&output).context("validate measured CLI startup output")?;
        durations.push(elapsed);
        last_output = Some(output);
    }

    let output = last_output.context("missing measured CLI startup output")?;

    Ok(CliStartupReport {
        generated_unix_seconds: unix_seconds(),
        git_commit: git_commit(),
        host_os: env::consts::OS.to_string(),
        host_arch: env::consts::ARCH.to_string(),
        command,
        runs: config.runs,
        warmups: config.warmups,
        durations,
        stdout_sha256: sha256_hex(&output.stdout),
        stderr_sha256: sha256_hex(&output.stderr),
    })
}

fn run_once(config: &Config) -> Result<Output> {
    let manifest = path_string(&config.manifest);
    Command::new(&config.layer36)
        .args([
            "run",
            "--auto-grant",
            "--manifest",
            manifest.as_str(),
            "--test-time",
            FIXED_TIME_MILLIS,
            "--test-locale",
            FIXED_LOCALE,
            "--test-timezone",
            FIXED_TIMEZONE,
        ])
        .arg(&config.wasm)
        .output()
        .with_context(|| format!("run {}", config.layer36.display()))
}

fn validate_output(output: &Output) -> Result<()> {
    if !output.status.success() {
        bail!(
            "command failed with status {:?}\nstdout:\n{}\nstderr:\n{}",
            output.status.code(),
            String::from_utf8_lossy(&output.stdout),
            String::from_utf8_lossy(&output.stderr)
        );
    }

    if output.stdout != EXPECTED_STDOUT.as_bytes() {
        bail!(
            "unexpected stdout\nexpected:\n{}\nactual:\n{}",
            EXPECTED_STDOUT,
            String::from_utf8_lossy(&output.stdout)
        );
    }

    if !output.stderr.is_empty() {
        bail!(
            "unexpected stderr:\n{}",
            String::from_utf8_lossy(&output.stderr)
        );
    }

    Ok(())
}

fn command_text(config: &Config) -> String {
    [
        path_string(&config.layer36),
        "run".to_string(),
        "--auto-grant".to_string(),
        "--manifest".to_string(),
        path_string(&config.manifest),
        "--test-time".to_string(),
        FIXED_TIME_MILLIS.to_string(),
        "--test-locale".to_string(),
        FIXED_LOCALE.to_string(),
        "--test-timezone".to_string(),
        FIXED_TIMEZONE.to_string(),
        path_string(&config.wasm),
    ]
    .join(" ")
}

fn path_string(path: &Path) -> String {
    path.display().to_string()
}

fn sha256_hex(bytes: &[u8]) -> String {
    let mut hasher = Sha256::new();
    hasher.update(bytes);
    format!("{:x}", hasher.finalize())
}

fn unix_seconds() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_secs()
}

fn git_commit() -> String {
    Command::new("git")
        .args(["rev-parse", "--short", "HEAD"])
        .output()
        .ok()
        .and_then(|output| {
            if output.status.success() {
                Some(String::from_utf8_lossy(&output.stdout).trim().to_string())
            } else {
                None
            }
        })
        .filter(|value| !value.is_empty())
        .unwrap_or_else(|| "unknown".to_string())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn stats_are_computed_from_sorted_duration_values() {
        let stats = StartupStats::from_durations(&[
            Duration::from_nanos(30),
            Duration::from_nanos(10),
            Duration::from_nanos(20),
        ]);

        assert_eq!(stats.min_ns, 10);
        assert_eq!(stats.median_ns, 20);
        assert_eq!(stats.mean_ns, 20);
        assert_eq!(stats.max_ns, 30);
    }

    #[test]
    fn default_layer36_path_uses_platform_exe_suffix() {
        let path = default_layer36_path();
        let expected = format!("layer36{}", env::consts::EXE_SUFFIX);
        assert_eq!(
            path.file_name().and_then(|name| name.to_str()),
            Some(expected.as_str())
        );
    }
}
